package candidates;

public class Candidates {
	
	private String name;
	private float height;
	private float weight;
	private float score;
	
	// Getter to get the Name of Candidate
	public String getName() {
		
		return name;
		
	}
	
	// Setter to set the Name of Candidate
	public void setName(String name) {
		
		this.name = name;
		
	}
	
	// Getter to get the Height of Candidate
	public float getHeight() {
		
		return height;
		
	}
	
	// Setter to set the Height of Candidate
	public void setHeight(float height) {
		
		this.height = height;
		
	}
	
	// Getter to get the Weight of Candidate
	public float getWeight() {
		
		return weight;
		
	}
	
	// Setter to set the Weight of Candidate
	public void setWeight(float weight) {
		
		this.weight = weight;
		
	}
	
	// Getter to get the Score of Candidate
	public float getScore() {
		
		return score;
		
	}
	
	// Setter to set the Score of Candidate
	public void setScore(float score) {
		
		this.score = score;
		
	}

}
